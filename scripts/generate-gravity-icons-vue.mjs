#!/usr/bin/env node
// Generates playground/src/gravity-icons-vue.ts with one named PascalCase
// export per icon in @iconify-json/gravity-ui. Each export is a Vue
// functional component that pulls its body from the offline Iconify JSON
// and renders inline via `h('svg', { innerHTML })`. Same approach as
// HeroUI's docs site (apps/docs/src/components/iconify.tsx). Idempotent.

import { readFileSync, writeFileSync } from 'node:fs'
import path from 'node:path'
import { fileURLToPath } from 'node:url'

const __dirname = path.dirname(fileURLToPath(import.meta.url))
const REPO = path.resolve(__dirname, '..')
const COLLECTION = path.join(REPO, 'playground/node_modules/@iconify-json/gravity-ui/icons.json')
const OUT = path.join(REPO, 'playground/src/gravity-icons-vue.ts')

const collection = JSON.parse(readFileSync(COLLECTION, 'utf8'))
const defaultWidth = collection.width ?? 16
const defaultHeight = collection.height ?? 16

function pascalCase (kebab) {
  return kebab.replace(/(^|-)([a-z0-9])/g, (_, _d, c) => c.toUpperCase())
}

const seen = new Set()
const entries = []
for (const name of Object.keys(collection.icons).sort()) {
  const pascal = pascalCase(name)
  if (seen.has(pascal) || !/^[A-Z][A-Za-z0-9_$]*$/.test(pascal)) continue
  seen.add(pascal)
  entries.push({ name: pascal, key: name })
}

const header = `/* AUTO-GENERATED by scripts/generate-gravity-icons-vue.mjs — do not edit by hand.
 *
 * Vue functional components for every @iconify-json/gravity-ui icon, named
 * to match the React-side \`@gravity-ui/icons\` imports (PascalCase). Mirrors
 * HeroUI's own docs approach (apps/docs/src/components/iconify.tsx) — pull
 * icon body data from the offline iconify JSON, render inline via h('svg').
 *
 * Keeping a named-export shim (rather than \`<Icon icon="gravity-ui:name" />\`)
 * means the auto-ported Vue demos import the same identifiers the React-side
 * demos do, so the porter stays simple and side-by-side reads cleanly.
 */

import { h, type FunctionalComponent } from 'vue'
import gravityCollection from '@iconify-json/gravity-ui/icons.json'

interface IconifyIconData {
  body: string
  width?: number
  height?: number
}
interface IconifyJson {
  icons: Record<string, IconifyIconData>
  width?: number
  height?: number
}

const collection = gravityCollection as IconifyJson
const W = collection.width ?? ${defaultWidth}
const H = collection.height ?? ${defaultHeight}

type IconProps = Record<string, any>

function gravityIcon (key: string): FunctionalComponent<IconProps> {
  const data = collection.icons[key]
  if (!data) return (props) => h('span', { ...props, 'data-missing-icon': key })
  const viewBox = \`0 0 \${data.width ?? W} \${data.height ?? H}\`
  return (props) => h('svg', {
    xmlns: 'http://www.w3.org/2000/svg',
    viewBox,
    width: '1em',
    height: '1em',
    fill: 'none',
    ...props,
    innerHTML: data.body,
  })
}

`

const body = entries.map(({ name, key }) =>
  `export const ${name}: FunctionalComponent<IconProps> = /* @__PURE__ */ gravityIcon('${key}')`
).join('\n')

writeFileSync(OUT, header + body + '\n')
console.log(`Wrote ${OUT} — ${entries.length} icons from @iconify-json/gravity-ui`)
