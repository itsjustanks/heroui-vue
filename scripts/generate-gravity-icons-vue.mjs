#!/usr/bin/env node
// Generates playground/src/gravity-icons-vue.ts with one named export per
// SVG file in @gravity-ui/icons/svgs/. Each export is a Vue functional
// component that renders the SVG via `innerHTML`. Run after `npm install`
// updates the icon package; idempotent.

import { readFileSync, writeFileSync, readdirSync } from 'node:fs'
import path from 'node:path'
import { fileURLToPath } from 'node:url'

const __dirname = path.dirname(fileURLToPath(import.meta.url))
const REPO = path.resolve(__dirname, '..')
const SVG_DIR = path.join(REPO, 'playground/node_modules/@gravity-ui/icons/svgs')
const OUT = path.join(REPO, 'playground/src/gravity-icons-vue.ts')

function pascalCase (kebab) {
  return kebab.replace(/(^|-)([a-z0-9])/g, (_, _d, c) => c.toUpperCase())
}

function extractInner (svg) {
  const m = svg.match(/<svg[^>]*>([\s\S]*?)<\/svg>/)
  return m ? m[1] : svg
}

const seen = new Set()
const entries = []
for (const file of readdirSync(SVG_DIR).sort()) {
  if (!file.endsWith('.svg')) continue
  const base = file.replace(/\.svg$/, '')
  const name = pascalCase(base)
  if (seen.has(name)) continue
  // Skip names that aren't valid JS identifiers (numeric leading char etc.).
  if (!/^[A-Z][A-Za-z0-9_$]*$/.test(name)) continue
  seen.add(name)
  const raw = readFileSync(path.join(SVG_DIR, file), 'utf8')
  const inner = extractInner(raw).replace(/`/g, '\\`').replace(/\$\{/g, '\\${')
  entries.push({ name, inner })
}

const header = `/* AUTO-GENERATED by scripts/generate-gravity-icons-vue.mjs — do not edit by hand.
 *
 * Vue functional components for every @gravity-ui/icons SVG. The Vue ports of
 * the upstream React demos import from here so calls like \`<ShoppingBag />\`
 * resolve to a real Vue component (the React package itself ships React
 * components which stringify to "[object Object]" inside Vue TSX).
 */

import { h, type FunctionalComponent } from 'vue'

type IconProps = Record<string, any>

function gravityIcon (inner: string): FunctionalComponent<IconProps> {
  return (props) => h('svg', {
    xmlns: 'http://www.w3.org/2000/svg',
    viewBox: '0 0 16 16',
    width: '1em',
    height: '1em',
    fill: 'none',
    ...props,
    innerHTML: inner,
  })
}

`

const body = entries.map(({ name, inner }) =>
  `export const ${name}: FunctionalComponent<IconProps> = /* @__PURE__ */ gravityIcon(\`${inner}\`)`
).join('\n')

writeFileSync(OUT, header + body + '\n')
console.log(`Wrote ${OUT} — ${entries.length} icons`)
